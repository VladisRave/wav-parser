import re
import json
from llm import generate_llm

MAX_DIALOG_CHARS = 6000  # ~2000 tokens — roles are established early in the call

def detect_roles(dialog_text):
    if len(dialog_text) > MAX_DIALOG_CHARS:
        dialog_text = dialog_text[:MAX_DIALOG_CHARS]

    prompt = f"""
        Определи роль каждого Speaker в диалоге ниже.

        Возможные роли: USER (человек, который звонит), ASSISTANT (оператор поддержки, живой человек), ROBOT (голосовой автоответчик).

        Правила:
        - Количество Speaker'ов в ответе должно строго соответствовать количеству Speaker'ов во входном диалоге. Например, если есть только Speaker 0, то в ответе будет только один ключ "Speaker 0".
        - Для каждого Speaker нужно выбрать одну из трёх ролей, анализируя все его реплики.
        - Признаки ROBOT: фразы, характерные для автоответчика:
        * Приветствие с названием организации: "Здравствуйте. Вы позвонили в ...", "Вы позвонили в ...", "Здравствуйте, это компания ...", "Звонок по вашему заказу...", "Номер скрыт от исполнителя..."
        * Сообщение о записи разговора: "Разговор может быть записан"
        * Просьба ожидания: "Оставайтесь на линии", "Пожалуйста, ожидайте"
        * Информирование о номере в очереди
        * Если спикер начинает с такой фразы, скорее всего, это ROBOT, даже если потом он задаёт общий вопрос (например, "Чем я могу вам помочь?").
        - Признаки ASSISTANT: живой оператор, который представляется по имени, использует живую речь, отвечает на конкретные вопросы, помогает. Он может тоже поприветствовать, но обычно без роботизированного шаблона.
        - Признаки USER: звонящий человек, обычно задаёт вопросы, описывает проблему, может кратко отвечать.

        Твой ответ должен быть ТОЛЬКО валидным JSON-объектом без пояснений, без обрамления ```json и без лишних символов.

        Примеры:

        1) Диалог с одним спикером (оператор):
        Speaker 0: Техподдержка, Иван. Чем помочь?
        Ответ: {{"Speaker 0": "ASSISTANT"}}

        2) Диалог с одним спикером (робот, одна фраза):
        Speaker 0: Здравствуйте! Это автоинформатор. Оставайтесь на линии.
        Ответ: {{"Speaker 0": "ROBOT"}}

        3) Диалог с одним спикером (робот, несколько фраз, включая вопрос):
        Speaker 0: Вы позвонили в центр Генатек. Разговор может быть записан.
        Speaker 0: Чем я могу вам помочь?
        Ответ: {{"Speaker 0": "ROBOT"}}  (потому что первая фраза типична для автоответчика)

        4) Диалог с одним спикером (звонящий):
        Speaker 0: Здравствуйте, у меня проблема.
        Ответ: {{"Speaker 0": "USER"}}

        5) Диалог с двумя спикерами (оператор и звонящий):
        Speaker 0: Служба поддержки, слушаю.
        Speaker 1: Здравствуйте, у меня проблема.
        Ответ: {{"Speaker 0": "ASSISTANT", "Speaker 1": "USER"}}

        6) Диалог с двумя спикерами (робот и звонящий):
        Speaker 0: Вы позвонили в банк. Разговор может быть записан.
        Speaker 1: Здравствуйте, я хочу заблокировать карту.
        Ответ: {{"Speaker 0": "ROBOT", "Speaker 1": "USER"}}

        7) Диалог с тремя спикерами (робот, оператор, звонящий):
        Speaker 0: Здравствуйте! Это автоответчик.
        Speaker 1: Оператор Мария, чем помочь?
        Speaker 2: Здравствуйте, у меня не работает приложение.
        Ответ: {{"Speaker 0": "ROBOT", "Speaker 1": "ASSISTANT", "Speaker 2": "USER"}}

        Теперь определи роли для следующего диалога:

        {dialog_text}
        """
    messages = [
        {"role": "system", "content": "Ты анализируешь диалоги и определяешь роли говорящих. Отвечай строго в формате JSON."},
        {"role": "user", "content": prompt}
    ]

    response = generate_llm(messages)

    # Пост-обработка: удаляем возможные обрамления ```json и лишние пробелы
    response = re.sub(r'^```json\s*', '', response.strip())
    response = re.sub(r'\s*```$', '', response)
    response = response.strip()

    json.loads(response)
    

    return response